                    <h1>Patient Care & Facilities</h1>
                </div>
                <div>
                    <p>At K Care Multispeciality Hospital, we are committed to providing world-class medical care supported by state-of-the-art facilities and patient-centered services. We prioritize comfort, safety, and personalized care for all our patients. Our hospital offers a comprehensive range of medical services to ensure that all your healthcare needs are met with professionalism and compassion.</p>
                </div>
            </div>
        </div>
        <div class="row">
            <div class="col-md-12">
                <h3 class="py-4">Our Comprehensive Medical Facilities</h3>
                <p>1. <b> 24/7 Emergency Care :</b>
                Our fully equipped Emergency Department is operational 24/7 to handle a wide range of medical emergencies, from trauma to acute illnesses. Our dedicated team of emergency physicians, nurses, and paramedics is trained to deliver life-saving care with speed and precision.
                </p> 
                <p>2. <b> Advanced Diagnostic and Imaging Services :</b>
                Our Diagnostic and Imaging Department is outfitted with the latest technology, including digital X-rays, MRI, CT scans, ultrasound, mammography, and fluoroscopy. These advanced tools allow for accurate diagnosis and help our physicians create tailored treatment plans for each patient.
                </p>
                <p>3. <b> Inpatient Services & Private Rooms :</b> 
                K Care provides comfortable inpatient facilities, offering both private and semi-private rooms. Our patient rooms are equipped with modern amenities, including adjustable beds, entertainment systems, and en-suite bathrooms, ensuring a restful and comfortable stay for our patients.
                </p>
                <p>4. <b> Intensive Care Unit (ICU) :</b>
                Our Intensive Care Unit (ICU) offers round-the-clock monitoring and advanced care for critically ill patients. Staffed with experienced intensivists and critical care nurses, the ICU is equipped with the latest life-support technologies, including ventilators, cardiac monitors, and specialized equipment for trauma and post-surgical care.
                </p>
                <p>5. <b> Operating Theaters (OT) :</b>
                Our state-of-the-art Operating Theaters are equipped with advanced surgical technology, including minimally invasive systems for laparoscopic and robotic surgeries. We ensure that our surgical teams have access to the best tools for performing complex procedures with precision and safety.
                </p>
                <p>6. <b> Outpatient Services :</b> 
                We offer a full range of outpatient services, including consultations, diagnostic tests, minor procedures, and follow-up appointments. Our outpatient department is designed to ensure minimal waiting times, with dedicated care coordinators to assist patients at every step of their healthcare journey.
                </p>
                <p>7. <b> Maternity and Neonatal Care :</b>
                Our Maternity and Neonatal Care units provide comprehensive support for expectant mothers and newborns. Our labor and delivery suites are equipped with advanced birthing equipment, and our Neonatal Intensive Care Unit (NICU) ensures that premature or critically ill newborns receive the highest level of care.
                </p>
                <p>8. <b> 24/7 Pharmacy :</b>
                Our in-house pharmacy is available 24/7, providing access to a wide range of prescription medications and over-the-counter drugs. Our pharmacists work closely with healthcare providers to ensure that patients receive the correct medications and proper guidance on usage.
                </p>
                <p>9. <b> Laboratory Services :</b>
                Our on-site laboratories offer a comprehensive range of diagnostic tests, from routine blood work to advanced pathology services. We maintain the highest standards of accuracy and safety, ensuring timely results to support the diagnosis and management of various medical conditions.
                </p>
                <p>10.<b> 24/7 Ambulance Services :</b> 
                Our fleet of ambulances is equipped with life-saving equipment and staffed by skilled paramedics. We provide emergency transportation services around the clock, ensuring that patients receive immediate medical attention en route to our hospital.
                </p>
                <p>11.<b> Dialysis Center :</b>
                Our Dialysis Center offers comprehensive care for patients with chronic kidney disease, providing both hemodialysis and peritoneal dialysis services. We prioritize patient comfort and safety, ensuring that each treatment is conducted with precision and care.
                </p>
                <p>12.<b> Physiotherapy & Rehabilitation :</b> 
                Our Rehabilitation Center offers a full range of physiotherapy services designed to help patients recover from surgery, injury, or chronic conditions. Our expert physiotherapists use personalized treatment plans, including physical exercises, electrotherapy, and manual therapy, to restore mobility and function.
                </p>
                <p>13.<b> Mental Health & Counseling Services :</b> 
                Our Psychiatry and Mental Health Department provides compassionate care for patients dealing with mental health conditions, including stress, anxiety, depression, and more. We offer confidential counseling, therapy, and medication management in a supportive and caring environment.
                </p>
